public class Calc {
    int a;
    int b;

    public Calc(int a, int b) {
        this.a = a;
        this.b = b;
    }

    public int summ(int a, int b){
        return a + b;
    }

    public int diff(int a, int b){
        return a - b;
    }

    public int mult(int a, int b){
        return a * b;
    }
}
